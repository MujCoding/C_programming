#include <stdio.h>
int main (){
    // increment operator
    // ++i i++
    int i=1;
    // i++ (post increament)
    printf("%d \n" , i++); // use , then increase
    printf("%d \n " , i);

    int j=1;
    // j++ (pre increament)
    printf("%d\n" ,++j); // increase , then use
    printf("%d\n" ,j);
    return 0;
    // decrement operator 
    // --i i--
    
}